package MyCod.StudySkillbox;

public class Modul_3_1 {
    public static void main(String[] args) {
       for (int i = 15; i <= 40 ; i = i + 1) {
            System.out.println(i);
            if (i == 16) {
                System.out.println("Вы можете обучаться вождению");
            }
            if (i == 18) {
                System.out.println("Вы можете получить водительские права");
            }
            if (i == 35) {
                System.out.println("Вы можете баллотироваться на пост президента");
            }
        }

    }
}
